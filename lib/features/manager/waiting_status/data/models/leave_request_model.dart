import 'dart:convert';
import '../../domain/entities/entities.dart';

List<LeaveRequestManagerModel> leaveRequestManagerFromJson(String str) => List<LeaveRequestManagerModel>.from(json.decode(str).map((x) => LeaveRequestManagerModel.fromJson(x)));

String leaveRequestManagerToJson(List<LeaveRequestManagerModel> data) => json.encode(List<dynamic>.from(data.map((x) => x.toJson())));

class LeaveRequestManagerModel extends LeaveRequestManager{

  const LeaveRequestManagerModel({
    required int? idLeave,
    required int? idLeaveType,
    required String? description,
    required DateTime? start,
    required DateTime? end,
    required int? idEmp,
    required double? used,
    required int? quota,
    required double? balance,
    required double? remaining,
    required int? idManagerEmployee,
    required dynamic approveDate,
    required dynamic isApprove,
    required int? isFullDay,
    required dynamic workingStart,
    required dynamic workingEnd,
    required int? isActive,
    required DateTime? createDate,
    required dynamic isWithdraw,
    required dynamic filename,
    required dynamic commentManager,
    required String? name,
    required String? firstname,
    required String? lastname,
    required String? positionsName,
    required dynamic departmentName,
    required String? startText,
    required String? endText,
    required dynamic approveDateText,
    required String? createLeaveText,
  }):super(
    idLeave : idLeave,
    idLeaveType : idLeaveType,
    description : description,
    start : start,
    end : end,
    idEmp : idEmp,
    used : used,
    quota : quota,
    balance : balance,
    remaining : remaining,
    idManagerEmployee : idManagerEmployee,
    approveDate : approveDate,
    isApprove : isApprove,
    isFullDay : isFullDay,
    workingStart : workingStart,
    workingEnd : workingEnd,
    isActive : isActive,
    createDate : createDate,
    isWithdraw : isWithdraw,
    filename : filename,
    commentManager : commentManager,
    name : name,
    firstname : firstname,
    lastname : lastname,
    positionsName : positionsName,
    departmentName : departmentName,
    startText : startText,
    endText : endText,
    approveDateText : approveDateText,
    createLeaveText : createLeaveText,
  );

  factory LeaveRequestManagerModel.fromJson(Map<String, dynamic> json) => LeaveRequestManagerModel(
    idLeave: json["idLeave"],
    idLeaveType: json["idLeaveType"],
    description: json["description"],
    start: json["start"] == null ? null : DateTime.parse(json["start"]),
    end: json["end"] == null ? null : DateTime.parse(json["end"]),
    idEmp: json["idEmp"],
    used: json["used"]?.toDouble(),
    quota: json["quota"],
    balance: json["balance"]?.toDouble(),
    remaining: json["remaining"]?.toDouble(),
    idManagerEmployee: json["idManagerEmployee"],
    approveDate: json["approveDate"],
    isApprove: json["isApprove"],
    isFullDay: json["isFullDay"],
    workingStart: json["workingStart"],
    workingEnd: json["workingEnd"],
    isActive: json["isActive"],
    createDate: json["createDate"] == null ? null : DateTime.parse(json["createDate"]),
    isWithdraw: json["isWithdraw"],
    filename: json["filename"],
    commentManager: json["commentManager"],
    name: json["name"],
    firstname: json["firstname"],
    lastname: json["lastname"],
    positionsName: json["positionsName"],
    departmentName: json["departmentName"],
    startText: json["startText"],
    endText: json["endText"],
    approveDateText: json["approveDateText"],
    createLeaveText: json["createLeaveText"],
  );

  Map<String, dynamic> toJson() => {
    "idLeave": idLeave,
    "idLeaveType": idLeaveType,
    "description": description,
    "start": start?.toIso8601String(),
    "end": end?.toIso8601String(),
